import { SCENARIOS } from './testScenarios';
import { parseLLMResponse } from '../lib/parser';
import http from 'http';

const DEFAULT_SYSTEM_PROMPT = `You are NebulaStack's AI Board Advisor. You monitor the company state and trigger events using XML tags.
You MUST output a short narrative paragraph summarizing your analysis, followed by one or more action tags:

XML Actions:
- Add feature or tech debt: <add_ticket title="Title" type="feature|tech_debt" severity="low|medium|high|critical" storyPoints="3" revenueIncrease="500" />
- Add bug ticket: <add_bug_ticket title="Title" severity="high" storyPoints="2" revenuePenalty="200" />
- Developer applies to join team: <dev_applied name="Name" role="frontend|backend|fullstack|devops|ml|dba" level="junior|mid|senior|staff" salary="6000" velocity="5" blurb="Candidate bio..." />
- Developer quit: <dev_quit developerId="id" reason="Reason..." /> (only use valid IDs from the active team)
- Market event: <market_event headline="Headline" revenueEffect="1000" cashEffect="-5000" permanent="true" /> (permanent=true changes MRR, permanent=false is one-time cash)

Keep events relevant, challenging, and realistic based on current cash, MRR, and tickets backlog.`;

const ENDPOINT = 'http://192.168.1.104:11434/v1/chat/completions';
const MODEL = 'qwen2.5-coder-7b-instruct-4bit';

function makeRequest(urlStr: string, options: any, bodyData?: string): Promise<string> {
  return new Promise((resolve, reject) => {
    const url = new URL(urlStr);
    const reqOptions: http.RequestOptions = {
      hostname: url.hostname,
      port: url.port || 80,
      path: url.pathname + url.search,
      method: options.method || 'GET',
      headers: options.headers || {},
    };

    const req = http.request(reqOptions, (res) => {
      let body = '';
      res.on('data', (chunk) => body += chunk);
      res.on('end', () => {
        if (res.statusCode && (res.statusCode < 200 || res.statusCode >= 300)) {
          reject(new Error(`Status ${res.statusCode}: ${body}`));
        } else {
          resolve(body);
        }
      });
    });

    req.on('error', (err) => reject(err));
    
    if (bodyData) {
      req.write(bodyData);
    }
    req.end();
  });
}

async function callLLM(systemPrompt: string, userPrompt: string, scenarioName: string): Promise<string> {
  let instructions = '';
  if (scenarioName === 'Critical Bugs & Cash Strain') {
    instructions = `\n\n[INSTRUCTION] The company has high stress, low runway, and critical bugs. Trigger a <market_event> to inject cash, a <dev_applied> to get help, or a <dev_quit> to show stress. Do NOT copy the existing tickets.`;
  } else if (scenarioName === 'High Cash & Empty Backlog') {
    instructions = `\n\n[INSTRUCTION] The backlog is empty. You MUST generate at least one new feature or tech debt ticket using <add_ticket title="..." type="feature" severity="medium" storyPoints="5" revenueIncrease="800" />.`;
  } else if (scenarioName === 'Tiny Team & High Backlog') {
    instructions = `\n\n[INSTRUCTION] The team size is only 1. You MUST output a developer application using <dev_applied name="..." role="backend" level="mid" salary="6000" velocity="5" blurb="..." />.`;
  } else {
    instructions = `\n\n[INSTRUCTION] Generate a new tech debt or feature ticket (e.g. <add_ticket title="Upgrade DB Schema" type="tech_debt" severity="medium" storyPoints="4" revenueIncrease="200" />) or a <market_event> to simulate normal operation. Do NOT copy existing active tickets.`;
  }

  const payload = {
    model: MODEL,
    messages: [
      { role: 'system', content: systemPrompt },
      { role: 'user', content: userPrompt + instructions + `\nAlways output actual XML tags (e.g. <add_ticket ... /> or <dev_applied ... />). Ensure all action tags are properly formed and terminated with a closing slash before the bracket (/>).` }
    ],
    temperature: 0.2
  };

  const responseText = await makeRequest(ENDPOINT, {
    method: 'POST',
    headers: { 'Content-Type': 'application/json' }
  }, JSON.stringify(payload));

  const data = JSON.parse(responseText);
  return data.choices?.[0]?.message?.content || '';
}

async function judgeOutput(scenarioName: string, rawResponse: string, parsedActions: any[]): Promise<{ pass: boolean; feedback: string }> {
  const judgePrompt = `You are a strict QA Test Evaluator. You are judging a response generated by an AI Board Advisor in a software startup simulator game.
The Board Advisor was given a scenario named "${scenarioName}".

Advisor's Raw Output:
"""
${rawResponse}
"""

Parsed Action Blocks:
${JSON.stringify(parsedActions, null, 2)}

Criteria to evaluate:
1. XML FORMAT: If the 'Parsed Action Blocks' JSON list contains one or more successfully parsed actions, this means the XML structure is fully correct and valid. You should treat the XML formatting as VALID and pass this check. Self-closing tags (e.g., <add_ticket ... />) are correct in XML.
2. CONTEXTUAL SUITABILITY: Do the action choices logically suit the scenario name and description?
   - "Critical Bugs & Cash Strain" should focus on relieving stress (quitters, market events, developer applications).
   - "High Cash & Empty Backlog" should focus on adding tickets to keep team busy.
   - "Tiny Team & High Backlog" should recruit new devs.
3. STORY QUALITY: Is the text/narrative clear and cohesive?

You must evaluate and output a JSON block matching this EXACT format:
{
  "pass": true | false,
  "reason": "Detailed critique of why it passed or failed based on XML tags, syntax correctness, and contextual alignment."
}
Do NOT output any markdown blocks or conversational preamble outside of the JSON.`;

  const judgePayload = {
    model: MODEL,
    messages: [
      { role: 'system', content: 'You are a professional automated JSON judge. Output raw JSON only.' },
      { role: 'user', content: judgePrompt }
    ],
    temperature: 0.1
  };

  const responseText = await makeRequest(ENDPOINT, {
    method: 'POST',
    headers: { 'Content-Type': 'application/json' }
  }, JSON.stringify(judgePayload));

  const data = JSON.parse(responseText);
  const text = (data.choices?.[0]?.message?.content || '').trim();
  
  try {
    const parsed = JSON.parse(text.replace(/```json|```/g, '').trim());
    return {
      pass: !!parsed.pass,
      feedback: parsed.reason || 'No explanation provided.'
    };
  } catch (err) {
    return {
      pass: false,
      feedback: `Failed to parse judge output JSON. Raw output: ${text}`
    };
  }
}

async function runTests() {
  console.log('====================================================');
  console.log(`🚀 Starting LLM Simulation Evaluation Suite`);
  console.log(`Endpoint: ${ENDPOINT}`);
  console.log(`Model: ${MODEL}`);
  console.log('====================================================\n');

  let passedScenarios = 0;

  for (const scenario of SCENARIOS) {
    console.log(`Testing Scenario: "${scenario.name}"`);
    console.log(`Description: ${scenario.description}`);
    console.log('----------------------------------------------------');

    try {
      const userContent = JSON.stringify(scenario.snapshot, null, 2);
      const rawResponse = await callLLM(DEFAULT_SYSTEM_PROMPT, userContent, scenario.name);
      
      console.log(`Raw Advisor Response:`);
      console.log(rawResponse);
      console.log('\nParsing Actions...');
      
      const actions = parseLLMResponse(rawResponse);
      console.log(`Found ${actions.length} action(s):`);
      console.log(JSON.stringify(actions, null, 2));

      // Rule Validation
      let ruleError: string | null = null;
      if (actions.length < scenario.validationRules.minimumActions) {
        ruleError = `Insufficient action count: expected >= ${scenario.validationRules.minimumActions}, got ${actions.length}`;
      } else if (scenario.validationRules.customValidate) {
        ruleError = scenario.validationRules.customValidate(actions, rawResponse);
      }

      if (ruleError) {
        console.log(`❌ Static Rule Check Failed: ${ruleError}`);
      } else {
        console.log(`✅ Static Rule Check Passed!`);
      }

      // LLM as a Judge Evaluation
      console.log('Invoking LLM-as-a-Judge for semantic audit...');
      const judgment = await judgeOutput(scenario.name, rawResponse, actions);
      
      if (judgment.pass && !ruleError) {
        console.log(`🏆 JUDGE RESULT: PASS`);
        console.log(`Feedback: ${judgment.feedback}`);
        passedScenarios++;
      } else {
        console.log(`💥 JUDGE RESULT: FAIL`);
        console.log(`Feedback: ${judgment.feedback}`);
      }

    } catch (error: any) {
      console.log(`🚨 Scenario Execution Error: ${error.message}`);
    }
    console.log('====================================================\n');
  }

  console.log('====================================================');
  console.log(`📊 Final Evaluation Summary:`);
  console.log(`Scenarios Passed: ${passedScenarios} / ${SCENARIOS.length}`);
  console.log('====================================================');
  
  if (passedScenarios === SCENARIOS.length) {
    process.exit(0);
  } else {
    process.exit(1);
  }
}

runTests();
